const giniSearch = Object.keys(ginis)
  .map(x => {
    return { gini: ginis[x] / 1000, name: x };
  })
  .sort((a, b) => b.gini - a.gini);

const closestCountry = giniValue => {
  const acceptable = giniSearch.filter(country => country.gini >= giniValue);
  return acceptable[acceptable.length - 1];
};

module.exports = closestCountry;
